package sector03.exam01;
//=========================================================
public class AssignmentOperator_Test {

	public static void main(String[] args) {
		// TODO Auto-generated method stub
		
		int res = 0;
		
		res += 10;
		System.out.println("res = " + res);
		
		res -= 5;
		System.out.println("res = " + res);
		
		res *= 3;
		System.out.println("res = " + res);
		
		res /= 5;
		System.out.println("res = " + res);
		
		res %= 3;
		System.out.println("res = " + res);
	

	}

}
